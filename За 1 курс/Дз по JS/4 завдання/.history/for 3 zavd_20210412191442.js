var number = parseInt(prompt("Введіть число від 1 до 12"));
// -------- sum
function season(number){
    if(number ==1){
       return alert("Січень");
    }
    else if(number ==2){
       return alert("Лютий");
    }
    else if(number ==3){
        return alert("Березень");
    }
    else if(number ==4){
        return alert("Квітень");
    }
    else if(number ==5){
       return alert("Травень");
    }
    else if(number ==6){
       return alert("Червень");
    }
    else if(number ==7){
        alert("Липень");
    }
    else if(number ==8){
        return  alert("Серпень");
    }
    
    else if(number ==9){
        return  alert("Вересень");
    }
    else if(number ==10){
        return alert("Жовтень");
    }
    else if(number ==11){
        return alert("Листопад");
    }
    else if(number ==12){
        return alert("Грудень");
    }
}
season(number);


